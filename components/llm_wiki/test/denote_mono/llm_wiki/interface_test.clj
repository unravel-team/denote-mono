(defn- clean-wiki-context
  "A wiki that passes every lint check: two cross-linked notes, each with
  an existing source file, plus a fresh index."
  []
  (let [context (make-context)
        source (str (temp-dir) "/src.txt")]
    (spit source "src")
    (llm-wiki/scaffold context)
    (write-note! context
                 {:id "20240101T000000",
                  :sig "1",
                  :title "Alpha",
                  :keywords ["ml"],
                  :body (str "Alpha body, see [Beta](denote:20240102T000000)."
                             "\n\n## Sources\n\n- [src](file:"
                             source
                             ")\n")})
    (write-note! context
                 {:id "20240102T000000",
                  :sig "1=1",
                  :title "Beta",
                  :keywords [],
                  :body (str "Beta body, see [Alpha](denote:20240101T000000)."
                             "\n\n## Sources\n\n- [src](file:"
                             source
                             ")\n")})
    (llm-wiki/regenerate-index context)
    context))

