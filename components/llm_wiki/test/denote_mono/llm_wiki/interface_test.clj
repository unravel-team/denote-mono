(defn- temp-dir
  []
  (str (Files/createTempDirectory "denote-llm-wiki-test"
                                  (make-array FileAttribute 0))))

(defn- make-context
  []
  (let [dir (temp-dir)]
    {:config (config/default-config),
     :silo {:name :wiki, :path dir, :llm-wiki true},
     :env {}}))

(defn- wiki-path
  [context relative]
  (str (get-in context [:silo :path]) "/" relative))

(defn- write-note!
  "Write a markdown-yaml Denote note with exact front matter, returning
  its absolute path. NOTE keys: :id :sig :title :keywords :body."
  [context {:keys [id sig title keywords body]}]
  (let [slug (str/lower-case (str/replace title #"[^A-Za-z0-9]+" "-"))
        filename (str id
                      (when sig (str "==" sig))
                      "--"
                      slug
                      (when (seq keywords) (str "__" (str/join "_" keywords)))
                      ".md")
        content (str "---\n"
                     "title:      \""
                     title
                     "\"\n"
                     "date:       2024-01-01\n"
                     "tags:       ["
                     (str/join ", " (map #(str "\"" % "\"") keywords))
                     "]\n"
                     "identifier: \"" id
                     "\"\n" (when sig (str "signature:  \"" sig "\"\n"))
                     "---\n\n" body)
        path (wiki-path context filename)]
    (fs/write-text path content)
    path))

(defn- scripted
  "Complete-fn popping canned assistant messages, recording requests."
  ([responses] (scripted responses (atom [])))
  ([responses requests]
   (let [remaining (atom (vec responses))]
     (fn [request]
       (swap! requests conj request)
       (let [msg (first @remaining)]
         (swap! remaining subvec 1)
         {:choices [{:message msg}]})))))

(defn- tool-call
  [id name args]
  {:role :assistant,
   :content nil,
   :tool-calls [{:id id,
                 :type "function",
                 :function {:name name, :arguments (json/write-str args)}}]})

(defn- problem-checks [result] (set (map :check (:problems result))))

